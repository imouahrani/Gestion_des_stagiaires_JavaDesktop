    private void bafficherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bafficherActionPerformed


        try {
            ps = co.connectbd().prepareStatement("select * from note");
            rs = ps.executeQuery();
            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Note");
            dtm.addColumn("Coeff");
            dtm.addColumn("Note * Coeff");
            while (rs.next()) {   
                
                dtm.addRow(new Object[]{rs.getObject("note"), rs.getObject("coeff"), (Float.parseFloat(rs.getObject("note").toString())* Integer.parseInt(rs.getObject("coeff").toString())) });
                tableNotes.setModel(dtm);
            }
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(Notes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bafficherActionPerformed

